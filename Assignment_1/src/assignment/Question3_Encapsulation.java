package assignment;

public class Question3_Encapsulation {

    private String name;
    private int age;
    private float salary;
    private int id;
    private String loc;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public int getempId() {
        return id;
    }

    public void setEmpId(int id) {
        this.id = id;
    }


    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public float getSalary() {
        return salary;
    }

    public void setSalary(float salary) {
        this.salary = salary;
    }
    
    public String getLocation() {
        return loc;
    }

    public void setLocation(String loc) {
        this.loc = loc;
    }
}

